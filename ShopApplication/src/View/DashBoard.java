    private void jMenuItem21ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem21ActionPerformed
        // TODO add your handling code here:
        this.dispose();
        UserModel userModel= new UserModel();
        UserController userController= new UserController();
        DisplayAccountInformation displayAccountInformation= new DisplayAccountInformation();
        try {
            userModel= userController.getInformation(Login.Username);           
            displayAccountInformation.getjTextFieldFullname().setText(userModel.getUsername());
            displayAccountInformation.getjTextFieldAddress().setText(userModel.getAddress());
            displayAccountInformation.getjTextFieldPhoneNumber().setText(userModel.getPhoneNumber());
            displayAccountInformation.getjTextFieldFullname().setEditable(false);
            displayAccountInformation.getjTextFieldAddress().setEditable(false);
            displayAccountInformation.getjTextFieldPhoneNumber().setEditable(false);
            displayAccountInformation.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(DashBoard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenuItem21ActionPerformed

