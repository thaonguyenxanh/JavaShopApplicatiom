    private void jMenuItemDisplayAllEmployeesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemDisplayAllEmployeesActionPerformed
        // TODO add your handling code here:
        DisplayAllEmployee displayAllEmployee= new DisplayAllEmployee();
        displayAllEmployee.setVisible(true);
        EmployeeController employeeController= new EmployeeController();
        ArrayList<Employee> employees= new ArrayList<>();
        employees= employeeController.getAllEmployees();
        DefaultTableModel defaultTableModel= (DefaultTableModel)displayAllEmployee.getjTableEmployee().getModel();
        for(Employee employee: employees){
           defaultTableModel.addRow(new Object[]{employee.getId(),employee.getFullname(), employee.getAddress(), employee.getPhoneNumber()});
        }
        this.dispose();
        
    }//GEN-LAST:event_jMenuItemDisplayAllEmployeesActionPerformed

